#!/usr/bin/env bash
# shellcheck disable=SC2016,SC2059

set -euo pipefail

SYNC_MANIFEST_PATH="c/mpi/one-sided/core/sync/Makefile_sync.mk"
SYNC_AM_PATH="c/mpi/one-sided/core/sync/Makefile.am"

BANNER_WARNING=$'# --------------------------------------------\n# AUTO-GENERATED FILE — DO NOT EDIT\n# This file is generated by autogen.sh\n# --------------------------------------------\n'

# Get REQUIRED var from a given Makefile.mk (errors if empty)
mget() {
  local list="$1" var="$2" val
  val=$(
    make -s -f - LIST="$list" VAR="$var" <<'MK'
include $(LIST)
print:
	@printf '%s\n' '$(value $(VAR))'
MK
) || true
  if [[ -z "${val// /}" ]]; then
    echo "ERROR: Required variable '$var' is missing or empty in $list" >&2
    exit 1
  fi
  echo "$val"
}

SYNC_LIB_NAME="$(mget "$SYNC_MANIFEST_PATH" OMB_SYNC_LIB_NAME)"

SYNC_TEMPLATE="$(mget "$SYNC_MANIFEST_PATH" OMB_SYNC_TEMPLATE)"
SYNC_DEFS="$(mget "$SYNC_MANIFEST_PATH" OMB_SYNC_DEFS)"
SYNC_LIBADD="$(mget "$SYNC_MANIFEST_PATH" OMB_SYNC_LIBADD)"

SYNC_TYPES="$(mget "$SYNC_MANIFEST_PATH" OMB_SYNC_TYPES)"

SYNC_DEPS="$SYNC_TEMPLATE $SYNC_DEFS $SYNC_LIBADD"

LIB_CANON="${SYNC_LIB_NAME%.la}_la"
LIB_OBJECTS_NAME="SYNC_TYPE_LOBJS"

{
    printf "$BANNER_WARNING\n"
    printf 'include $(top_srcdir)/c/mpi/Makefile_prologue.am\n\n'

    printf '%s = \\\n  ' "$LIB_OBJECTS_NAME"
    first=1
    for s_name in $SYNC_TYPES; do
        if [[ $first -eq 1 ]]; then first=0; else printf ' \\\n  '; fi
        printf 'sync_%s.lo' "$s_name"
    done

    printf "\n\nnoinst_LTLIBRARIES = ${SYNC_LIB_NAME}\n"
    printf "${LIB_CANON}_SOURCES =\n"
    printf "${LIB_CANON}_LIBADD = $SYNC_LIBADD \$($LIB_OBJECTS_NAME)\n"
    printf "\n${LIB_CANON}_DEPENDENCIES = $SYNC_LIBADD \$($LIB_OBJECTS_NAME)\n\n"

    for s_name in $SYNC_TYPES; do
        printf 'sync_%s.lo:' "$s_name"
        for dep in $SYNC_DEPS; do
          printf ' %s' $dep
        done
        printf '\n	@$(MKDIR_P) $(DEPDIR) \n'
        printf '	$(LTCOMPILE) $(AM_CFLAGS) -include %s ' "$SYNC_DEFS"
        printf ' -DOMB_SYNC_%s -MT $@ -MD -MP -MF $(DEPDIR)/$(@F).Tpo -c -o $@ %s \n\n' "$s_name" "$SYNC_TEMPLATE"
    done
} > "$SYNC_AM_PATH"
