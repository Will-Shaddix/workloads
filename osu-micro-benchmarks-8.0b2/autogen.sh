#!/usr/bin/env bash
# shellcheck disable=SC2016,SC2059

set -euo pipefail

# Arg processing
DO_AUTORECONF=1
while (($#)); do
  case "$1" in
    --no-autoreconf) DO_AUTORECONF=0 ;;
    --) shift; break ;;
    *) echo "Unknown option: $1" >&2; exit 2 ;;
  esac
  shift
done

# Constants
C_DIR="c"

ENTRIES_HEADER_NAME="entry_list.h"
ENTRY_RULES_NAME="entry.rules.am"
EXEC_RULES_NAME="executable.rules.am"

BANNER_WARNING_MAKE=$'# --------------------------------------------\n# AUTO-GENERATED FILE — DO NOT EDIT\n# This file is generated by autogen.sh\n# --------------------------------------------\n\n'
BANNER_WARNING_C=$'/*------------------------------------------\n  AUTO-GENERATED FILE — DO NOT EDIT \n  This file is generated by autogen.sh \n  ------------------------------------------*/\n\n'

# Get variable from makefile
mget() {
  local list="$1" var="$2" m_out
  local -i require="${3-1}"
  
  m_out=$(
    make -s -f - LIST="$list" VAR="$var" <<'MK'
include $(LIST)
print:
	@printf '%s\n' '$(value $(VAR))'
MK
) || true

  if (( require )) && [[ -z "${m_out//[[:space:]]/}" ]]; then
    echo "ERROR: Required variable '$var' is missing or empty in $list" >&2
    return 1
  fi
  printf '%s\n' "$m_out"
}

# Get OPTIONAL var (returns empty if unset)
mget_opt() {
  mget "$1" "$2" 0
}

# Returns 0 (true) if VAR is defined and non-empty in LIST
mhas() {
  [[ "$(mget_opt "$1" "$2")" =~ [^[:space:]] ]]
}

# To lowercase, non-alnum -> _
suite_key() {
  local s="$1"
  s="${s,,}"
  s="${s//[^a-z0-9]/_}"
  echo "$s"
}

emit_suite_entries_header() {
  local suite_dir="$1" suite_name="$2" suite_progs="$3"
  local key hdr i n cont
  key="$(suite_key "${suite_name}")"
  hdr="${suite_dir}/${ENTRIES_HEADER_NAME}"

  {
    printf "$BANNER_WARNING_C"

    printf "#define OMB_FOR_EACH_ENTRY_$key(X) \\"
    printf "\n"

    suite_count=0; n=0; for _ in $suite_progs; do n=$((n+1)); done
    for p_name in $suite_progs; do
      suite_count=$((suite_count+1)); cont=" \\"; [[ $suite_count -eq $n ]] && cont=""
      printf "  X(\"$key\", \"$p_name\", omb_entry_$p_name)%s\n" "$cont"
    done
    printf "\n#define OMB_COUNT_$key %d\n" "$suite_count"
  } > "${hdr}"
}

emit_domain_entries_header() {
  local header_name="$1" domain_dir="$2" keys_ref="$3" headers_ref="$4"
  declare -n domain_keys="$keys_ref"
  declare -n domain_headers="$headers_ref"
  local out="${domain_dir}/launcher/${header_name}"
  local k h i n cont
  {
    printf "$BANNER_WARNING_C"

    for h in "${domain_headers[@]}"; do
      printf "#include \"../$h\"\n"
    done

    printf "\n/* Single macro to expand all entries across all suites */\n"
    printf "#define OMB_FOR_EACH_ENTRY_ALL(X) \\"
    printf "\n"
    i=0; n=${#domain_keys[@]}
    for k in "${domain_keys[@]}"; do
      i=$((i+1)); cont=" \\"; [[ $i -eq $n ]] && cont=""
      printf "  OMB_FOR_EACH_ENTRY_%s(X)%s\n" "$k" "$cont"
    done

    printf '\n#define OMB_ENTRY_COUNT (\\\n'
    i=0; n=${#domain_keys[@]}
    for k in "${domain_keys[@]}"; do
      i=$((i+1))
      if [[ $i -lt $n ]]; then
        printf '  OMB_COUNT_%s + \' "$k"
      else
        printf '  OMB_COUNT_%s\' "$k"
      fi
      printf "\n"
    done
    printf ")\n"
  } > "$out"

  echo ">>> Wrote $out"
}

###############################################################################
# Main Execution
###############################################################################

printf ">>> Generating RMA Sync build targets\n\n"
./sync_gen.sh

# Discover all Makefiles
mapfile -t _ALL_MKS < <(find "$C_DIR" -type f -name '*.mk' | sort)

# Domains define OMB_DOMAIN_NAME
DOMAIN_LIST=()
for MKS in "${_ALL_MKS[@]}"; do
  if mhas "$MKS" OMB_DOMAIN_NAME; then
    DOMAIN_LIST+=("$MKS")
  fi
done

if [[ ${#DOMAIN_LIST[@]} -eq 0 ]]; then
  echo "No domains (Makefile.mk with OMB_DOMAIN_NAME) found under ${C_DIR}; skipping rule generation."
else
  for DOMAIN_MK in "${DOMAIN_LIST[@]}"; do
    # --- Per-domain ---
    DOMAIN_DIR="$(dirname "${DOMAIN_MK}")"
    DOMAIN_ENTRY_KEYS=()
    DOMAIN_ENTRY_HEADERS=()

    # --- Domain defaults ---
    DOMAIN_NAME="$(mget "${DOMAIN_MK}" OMB_DOMAIN_NAME)"
    DOMAIN_TEMPLATE="$(mget "${DOMAIN_MK}" OMB_DOMAIN_TEMPLATE)"
    DOMAIN_EXEC_MAIN="$(mget "${DOMAIN_MK}" OMB_DOMAIN_EXEC_MAIN)"
    DOMAIN_LIBS="$(mget_opt "${DOMAIN_MK}" OMB_DOMAIN_LIBS)"
    DOMAIN_MACROS="$(mget_opt "${DOMAIN_MK}" OMB_DOMAIN_MACROS)"
    DOMAIN_CFLAGS="$(mget_opt "${DOMAIN_MK}" OMB_DOMAIN_CFLAGS)"

    # --- Domain launcher ---
    LAUNCHER_RULES_PATH="$DOMAIN_DIR/launcher/Makefile.am"
    LAUNCHER_EXECUTABLE="$(mget "${DOMAIN_MK}" OMB_LAUNCHER_EXECUTABLE)"
    LAUNCHER_TITLE="$(mget "${DOMAIN_MK}" OMB_LAUNCHER_TITLE)"
    LAUNCHER_ENTRY_LIST_NAME="omb_$(suite_key "${DOMAIN_NAME}")_${ENTRIES_HEADER_NAME}"

    echo "=== Domain '${DOMAIN_NAME}' at ${DOMAIN_DIR}"

    # Emit per-domain launcher rules
    mkdir -p "$DOMAIN_DIR/launcher"
    {
      printf "$BANNER_WARNING_MAKE"
      printf 'include $(top_srcdir)/c/mpi/Makefile_prologue.am\n\n'
      printf "bin_PROGRAMS = ../$LAUNCHER_EXECUTABLE\n\n"
      printf '___%s_SOURCES = $(top_srcdir)/c/launcher/launcher.c $(top_srcdir)/c/launcher/omb_launcher_util.c \n\n' $LAUNCHER_EXECUTABLE
      printf '___%s_CFLAGS = \' $LAUNCHER_EXECUTABLE
      printf '\n  -I$(top_srcdir)/c/util -I$(top_builddir)/c/util \'
      printf "\n  -DOMB_ENTRIES_HEADER='\"%s\"' \\" $LAUNCHER_ENTRY_LIST_NAME
      printf "\n  -DEXECUTABLE_NAME='\"$LAUNCHER_EXECUTABLE\"' \\"
      printf "\n  -DBENCHMARK='$LAUNCHER_TITLE'\n\n"
      printf '___%s_LDADD =\n' $LAUNCHER_EXECUTABLE
    } > "$LAUNCHER_RULES_PATH"

    # Process suites
    SUITE_LIST=()
    for MKS in "${_ALL_MKS[@]}"; do
      if mhas "$MKS" OMB_SUITE_NAME && [[ "$MKS" == "$DOMAIN_DIR"* ]]; then
        SUITE_LIST+=("$MKS")
      fi
    done

    for SUITE_MK in "${SUITE_LIST[@]}"; do
      # Required
      SUITE_NAME="$(mget "$SUITE_MK" OMB_SUITE_NAME)"
      SUITE_PROGS="$(mget "$SUITE_MK" OMB_SUITE_PROGS)"
      SUITE_DIR="$DOMAIN_DIR/$SUITE_NAME"
      SUITE_DEF_FILE_PATH="\$(top_srcdir)/$DOMAIN_DIR/omb_${SUITE_NAME}_defs.h"

      mkdir -p $SUITE_DIR

      # Read suite opts (may be empty)
      BUILD_PROGS_IN_DOMAIN="$(mget_opt "$SUITE_MK" OMB_SUITE_BUILD_PROGS_IN_DOMAIN)"
      _SUITE_TEMPLATE="$(mget_opt "$SUITE_MK" OMB_SUITE_TEMPLATE)"
      _SUITE_LIBADD="$(mget_opt "$SUITE_MK" OMB_SUITE_LIBS)"
      _SUITE_MACROS="$(mget_opt "$SUITE_MK" OMB_SUITE_MACROS)"
      _SUITE_CFLAGS="$(mget_opt "$SUITE_MK" OMB_SUITE_CFLAGS)"

      # Override: Suite template > domain template
      SUITE_TEMPLATE="${_SUITE_TEMPLATE:-$DOMAIN_TEMPLATE}"

      # Merges: domain first, then suite additions
      SUITE_LIBADD="$DOMAIN_LIBS $_SUITE_LIBADD"
      SUITE_DEPS="$SUITE_DEF_FILE_PATH $SUITE_TEMPLATE $SUITE_LIBADD"
      SUITE_MACROS="$DOMAIN_MACROS $_SUITE_MACROS"
      SUITE_CFLAGS="$DOMAIN_CFLAGS $_SUITE_CFLAGS"

      # Turn merged macros into -D flags
      if [[ $SUITE_MACROS =~ [^[:space:]] ]]; then
        for m in $SUITE_MACROS; do SUITE_CFLAGS+=" -D$m"; done
      fi

      EXEC_RULES_PATH="$SUITE_DIR/$EXEC_RULES_NAME"
      ENTRY_RULES_PATH="$SUITE_DIR/$ENTRY_RULES_NAME"
      SUITE_AM_PATH="$SUITE_DIR/Makefile.am"

      echo ">>> [${DOMAIN_NAME}] Generating rules and header for suite '${SUITE_NAME}' in ${SUITE_DIR}"

      # --- Exec rules ---
      {
      printf "$BANNER_WARNING_MAKE"

      printf '%sdir = $(pkglibexecdir)/%s\n' "$SUITE_NAME" "${DOMAIN_DIR#*c/}"
      printf '%s_PROGRAMS = \\\n  ' "$SUITE_NAME"
      first=1
      for p_name in $SUITE_PROGS; do
        if [[ $first -eq 1 ]]; then first=0; else printf ' \\\n  '; fi
        [[ -n "$BUILD_PROGS_IN_DOMAIN" ]] && p_name="../$p_name";
        printf '%s' "$p_name"
      done
      printf '\n\n'

      for p_name in $SUITE_PROGS; do
        exec_name=$p_name
        [[ -n "$BUILD_PROGS_IN_DOMAIN" ]] && exec_name=$(suite_key "../$p_name");

        printf "${exec_name}_SOURCES  = $DOMAIN_EXEC_MAIN\n"
        printf "${exec_name}_CFLAGS = \$(AM_CFLAGS) -DOMB_ENTRY_SYM=omb_entry_$p_name"
        printf " -include ${SUITE_DEF_FILE_PATH} -DOMB_NAME_${p_name} ${SUITE_CFLAGS}\n"
        printf "${exec_name}_LDADD = ${p_name}_entry.lo "
        for libadd in $SUITE_LIBADD; do
          printf "$libadd "
        done

        printf "\n${exec_name}_DEPENDENCIES  = ${p_name}_entry.lo "
        for libadd in $SUITE_LIBADD; do
          printf "$libadd "
        done 
        printf "\n\n"
      done
      } > "$EXEC_RULES_PATH"

      # --- Entry rules ---
      {
        LIB_FILE="libomb_${SUITE_NAME}_entries.la"
        LIB_CANON="${LIB_FILE%.la}_la"
        LIB_OBJECTS_NAME="${SUITE_NAME}_ENTRY_LOBJS"
        SUITE_LIB_TARGETS=()

        printf "$BANNER_WARNING_MAKE"
        
        printf '%s = \\\n  ' "$LIB_OBJECTS_NAME"
        first=1
        for p_name in $SUITE_PROGS; do
          if [[ $first -eq 1 ]]; then first=0; else printf ' \\\n  '; fi
          printf '%s_entry.lo' "$p_name"
        done

        printf "\n\nnoinst_LTLIBRARIES = ${LIB_FILE}\n"
        printf "${LIB_CANON}_SOURCES =\n"
        printf "${LIB_CANON}_LIBADD = \$($LIB_OBJECTS_NAME)\n"
        printf "${LIB_CANON}_DEPENDENCIES = \$($LIB_OBJECTS_NAME)\n\n"

        for p_name in $SUITE_PROGS; do
          printf '%s_entry.lo: ' "$p_name"
          for dep in $SUITE_DEPS; do
            printf ' %s' $dep
          done
          printf '\n	@$(MKDIR_P) $(DEPDIR) \n'
          printf '	$(LTCOMPILE) $(AM_CFLAGS) $(CFLAGS) %s -include %s ' "$SUITE_CFLAGS" "$SUITE_DEF_FILE_PATH"
          printf ' -DOMB_ENTRY_SYM=omb_entry_%s -DOMB_NAME_%s -MT $@ -MD -MP -MF $(DEPDIR)/$(@F).Tpo -c -o $@ %s \n\n' "$p_name" "$p_name" "$SUITE_TEMPLATE"
        done

        # Ensure any runtime libs built in sibling dirs are created when building this suite directly.
        for libadd in $SUITE_LIBADD; do
          if [[ "$libadd" == *.la ]]; then
            lib_target="${libadd/\$\(top_srcdir\)/\$(top_builddir)}"
            # Avoid emitting duplicate rules.
            if [[ ! " ${SUITE_LIB_TARGETS[*]} " =~ " ${lib_target} " ]]; then
              SUITE_LIB_TARGETS+=("$lib_target")
              lib_dir="$(dirname "$lib_target")"
              lib_file="$(basename "$lib_target")"
              printf '%s:\n' "$lib_target"
              printf '\t$(MAKE) -C %s %s\n\n' "$lib_dir" "$lib_file"
            fi
          fi
        done
      } > "$ENTRY_RULES_PATH"

      # --- Suite Makefile.am ---
      {
        printf "$BANNER_WARNING_MAKE"
        printf 'include $(top_srcdir)/c/mpi/Makefile_prologue.am\n'
        printf 'include $(top_srcdir)/%s/executable.rules.am\n' $SUITE_DIR
        printf 'include $(top_srcdir)/%s/entry.rules.am\n' $SUITE_DIR
      } > "$SUITE_AM_PATH"

      # --- Add entry list to launcher libs ---
      {
        printf '___%s_LDADD += $(top_builddir)/%s/libomb_%s_entries.la\n' $LAUNCHER_EXECUTABLE $SUITE_DIR $SUITE_NAME
      } >> "$LAUNCHER_RULES_PATH"

      emit_suite_entries_header "${SUITE_DIR}" "${SUITE_NAME}" "${SUITE_PROGS}"
      DOMAIN_ENTRY_HEADERS+=( "$(basename "$SUITE_DIR")/${ENTRIES_HEADER_NAME}" )
      DOMAIN_ENTRY_KEYS+=( "$(suite_key "${SUITE_NAME}")" )
    done

    # Last launcher LDADD
    {
      for libadd in $DOMAIN_LIBS; do
          printf "___%s_LDADD += $libadd\n" $LAUNCHER_EXECUTABLE
        done
    } >> "$LAUNCHER_RULES_PATH"

    # Emit per-domain entries header
    emit_domain_entries_header $LAUNCHER_ENTRY_LIST_NAME $DOMAIN_DIR DOMAIN_ENTRY_KEYS DOMAIN_ENTRY_HEADERS
  done
fi

if (( DO_AUTORECONF )); then
  echo ">>> Running autoreconf -fi"
  autoreconf -fi
else
  echo ">>> Skipping autoreconf"
fi
